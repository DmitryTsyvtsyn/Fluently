enum class ThemeContrast(val title: Int, val description: Int) {
    DYNAMIC(R.string.dynamic, R.string.dynamic_contrast_description),
    LIGHT(R.string.light, R.string.light_contrast_description),
    MEDIUM(R.string.medium, R.string.medium_contrast_description),
    HIGH(R.string.high, R.string.high_contrast_description)
}

@SuppressLint("NewApi")
@Composable
fun InterFunnyTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    contrast: ThemeContrast = if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) ThemeContrast.DYNAMIC else ThemeContrast.LIGHT,
    content: @Composable () -> Unit
) {

    val colorScheme = if (darkTheme) {
        when(contrast) {
            ThemeContrast.DYNAMIC -> dynamicDarkColorScheme(LocalContext.current)
            ThemeContrast.LIGHT -> darkScheme
            ThemeContrast.MEDIUM -> mediumContrastDarkColorScheme
            ThemeContrast.HIGH -> highContrastDarkColorScheme
        }
    } else {
        when(contrast) {
            ThemeContrast.DYNAMIC -> dynamicLightColorScheme(LocalContext.current)
            ThemeContrast.LIGHT -> lightScheme
            ThemeContrast.MEDIUM -> mediumContrastLightColorScheme
            ThemeContrast.HIGH -> highContrastDarkColorScheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}